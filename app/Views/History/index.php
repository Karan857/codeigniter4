<p class="text-center fs-2 m-5 " style="height: 50vh">“นครปฐม” เป็นอู่อารยธรรมสำคัญที่มีประวัติความเป็นมายาวนานในแผ่นดินสุวรรณภูมิ 
จากหลักฐานทางประวัติศาสตร์กล่าวว่า เมืองนครปฐมแต่เดิมนั้นตั้งอยู่ริมทะเล เป็นเมืองเก่าแก่ 
มีความเจริญรุ่งเรืองมานับตั้งแต่สมัยสุวรรณภูมิ และเป็นราชธานีสำคัญในสมัยทวารวดี 
ในยุคนั้นนครปฐมเป็นแหล่งเผยแพร่อารยธรรมจากประเทศอินเดีย ซึ่งรวมทั้งพุทธศาสนา 
นครปฐมจึงเป็นศูนย์กลางของความเจริญ มีชนชาติต่างๆ อพยพเข้ามาตั้งถิ่นฐานอยู่เป็นจำนวนมาก</p>
